#include "calculator.hpp"



int calculator::add(int input1,int input2) const
{
    return  input1+input2;
}
int calculator::sub(int input1 ,int input2) const
{
    return input1-input2;
}
int calculator::mul(int input1,int input2) const
{
    return input1*input2;
}
int calculator::div(int input1,int input2) const
{
    return input1/input2;
}